package br.gov.incra.beneficiario.model;

public class NivelEscolaridade {
    Long idEscolaridade;
    String nivelEscolaridade;

    public Long getIdEscolaridade() {
        return this.idEscolaridade;
    }

    public void setIdEscolaridade(Long idEscolaridade) {
        this.idEscolaridade = idEscolaridade;
    }

    public String getNivelEscolaridade() {
        return this.nivelEscolaridade;
    }

    public void setNivelEscolaridade(String nivelEscolaridade) {
        this.nivelEscolaridade = nivelEscolaridade;
    }
}
